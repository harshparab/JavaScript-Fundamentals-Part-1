// Abstract equality operator
const age = 18;

if (age == 18) {
    console.log("Age is correct");
}
else {
    console.log("Age is incorrect");
}

// Strict equality operator
const ageOne = 18;

if (ageOne === 18) {
    console.log("Age is correct");
}
else {
    console.log("Age is incorrect");
}
